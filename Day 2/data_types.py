# Data Types

# String
user_name = "Ronald Harsh"
print(user_name[0])  # Returns the first letter of the given string.
print(user_name[-1])  # Returns the last character of the given string.
print(user_name[len(user_name) - 1])  # Also prints out the last character of the given string.

# Integer
user_age = 43
user_spouse_age = 30
print(user_age + user_spouse_age)

user_net_worth = 1_000_000
print(user_net_worth)

# Float (floating point number)
user_pi = 3.14159
print(user_pi)

# Boolean
user_employed = False
print(user_employed)
